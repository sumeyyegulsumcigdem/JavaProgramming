package day18_NestedLoop;

public class LoopsWithoutCurlyBraces {

    public static void main(String[] args) {

        for (int i = 0; i < 5; i++)
            System.out.println("Cydeo");
        System.out.println("Batch 25");

        System.out.println("--------------------------------");

        for (int i = 0; i < 5; i++) {
            System.out.println("Cydeo");
            System.out.println("Batch 25");
        }


    }
}
