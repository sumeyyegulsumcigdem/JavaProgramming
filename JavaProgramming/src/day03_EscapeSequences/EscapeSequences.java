package day03_EscapeSequences;

public class EscapeSequences {

    /*
    escape sequences: MUST BE given with double quote
    \n: starts a new line
    \t: paragraph space (tab),
    \\: single back slash
    \": double quote

     */

    public static void main(String[] args) {

        System.out.println("Java \nPython \nC#");

        System.out.println("----------------------");

        System.out.println("Hello Cydeo \nHow are you all today? \nIts nice to see you all! \nWhat class do we have next week?");

        System.out.println("------------------------");

        System.out.println("\tJava is Cool Programming Language");

        System.out.println("---------------------------");

        System.out.println("/ \\"); // in order to print one backward lash we need to give to backward slash

        System.out.println("--------------------------");

        System.out.println("My favourite TV Show is \"Game of Thrones\""); //in order to print double quote, we need to use \"

    }

}
